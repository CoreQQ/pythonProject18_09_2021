
age = int(input("Enter your age!"))
for baby in range(0, 2):
    if age == baby:
        print("You are a baby <3")
for child in range(2, 14):
    if age == child:
        print("You are a Child;)")
for teenager in range(14, 21):
    if age == teenager:
        print("You are a teenager)")
for adult in range(21, 101):
    if age == adult:
        print("You are a adult!")

